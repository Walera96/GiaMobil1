package com.spbutu.gia.auth.domain.enums;

/**
 * Роли пользователей в системе.
 * Хранится в БД как строка (VARCHAR) для читаемости.
 */
public enum UserRole {
    ADMIN,        // Администратор системы
    METHODIST,    // Методист (ведет справочники)
    SECRETARY,    // Секретарь ГЭК (создает заседания, протоколы)
    CHAIRMAN,     // Председатель ГЭК
    GEK_MEMBER,   // Член ГЭК (голосует)
    STUDENT,      // Студент (просмотр результатов)
    DEAN          // Деканат (предпросмотр и утверждение документов)
}
